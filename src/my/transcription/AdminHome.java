    public void userSelection(){
        list1.removeAll();
        HashMap<String, String> userMap = backend.createLogs();
        Iterator it = userMap.entrySet().iterator();
        while(it.hasNext()){
            HashMap.Entry pair = (HashMap.Entry) it.next();
            String name = (String) pair.getKey();
            list1.add(name);
        }
    }

